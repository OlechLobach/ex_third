while True:
    d1 = float(input("Введіть довжину першої діагоналі: "))
    d2 = float(input("Введіть довжину другої діагоналі: "))

    площа = (d1 * d2) / 2

    print(f"Площа ромба з діагоналями {d1} і {d2} дорівнює {площа}")

    продовжити = input("Бажаєте ввести нові числа? (так/ні): ")

    if продовжити.lower() != 'так':
        break
